# Creating a Function
# In Python a function is defined using the def keyword:

def my_function():
  print("Hello from a function")

# Calling a Function
# To call a function, use the function name followed by parenthesis:

def my_function():
  print("Hello from a function")

my_function()

# Arguments
def full_name(fname):
  print(f"Fullname of { fname } is { fname } Pawar")

full_name("Jayesh")
full_name("Suresh")
full_name("Mangesh")

